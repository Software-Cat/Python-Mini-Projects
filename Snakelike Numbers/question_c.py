# If 9 is first digit, second digit must be smaller than 9. So the second digit goes down.
# The third digit would have to go up, fourth down, fifth up, sixth down and seventh up.
# Since the seventh digit is going up, it must be larger than the sixth digit.
# However, if the seventh digit is 0 it cannot be larger than the sixth digit as 0 is the smallest digit.
# Therefore the last digit cannot be 0